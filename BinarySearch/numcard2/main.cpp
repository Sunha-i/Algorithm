#include <iostream>
#include <vector>
#include <algorithm>
#include <unordered_map>

using namespace std;

unordered_map<int, int> um;

int binarysearch(vector<int>& arr, int start, int end, int n) {

    int cnt = 0;

    while (start <= end) {
        int mid = (start + end) / 2;

        if (arr[mid] == n) {    // ì°¾ìœ¼ë©´? ì¢Œìš°ë¡œ ê²½ê³„ë¥¼ ì°¾ì•„ ëª‡ê°œì¸ì§€ ì•Œì•„ë‚´ê¸°
            int leftmid = mid;
            int rightmid = mid;
            while (leftmid--) {
                if (arr[leftmid] == n)  cnt++;
                else    break;
            }
            while (rightmid++ != arr.size()) {
                if (arr[rightmid] == n)  cnt++;
                else    break;
            }
            um.insert(make_pair(n, cnt+1));
            return cnt+1;
        }
        else if (arr[mid] > n)
            end = mid - 1;
        else
            start = mid + 1;
    }

    return cnt;
}

int main(int argc, const char * argv[]) {
    ios_base::sync_with_stdio(false);
    cin.tie(0);

    int N, M;
    vector<int> cardlist;

    cin >> N;
    for (int i = 0; i < N; i++) {
        int num;
        cin >> num;
        cardlist.push_back(num);
    }
    sort(cardlist.begin(), cardlist.end());

    cin >> M;
    for (int i = 0; i < M; i++) {
        int num;
        cin >> num;
        if (um.find(num) != um.end()) {
            cout << um[num] << " ";
            continue;
        }
        cout << binarysearch(cardlist, 0, N-1, num) << " ";
    }

    return 0;
}

